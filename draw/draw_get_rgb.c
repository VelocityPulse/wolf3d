#include "draw.h"

t_rgb	ft_get_rgb(int hexa)
{
	t_rgb	rgb;

	rgb.r = (hexa / (256 * 256)) % 256;
	rgb.g = (hexa / 256) % 256;
	rgb.b = hexa % 256;
	return (rgb);
}
