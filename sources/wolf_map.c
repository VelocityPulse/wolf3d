#include "../headers/header.h"

static void		malloc_map(t_wolf3d *w3d)
{
	int		i;

	i = -1;
	w3d->map = (int **)ft_memalloc(sizeof(int *) * 15);
	while (++i < 15)
		w3d->map[i] = (int *)ft_memalloc(sizeof(int) * 20);
	w3d->height = 15;
}

